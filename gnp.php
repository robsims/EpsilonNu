                    After beginning his collegiate marching career marching in the University of Delaware Fightin' Blue Hens Band, Parks transferred to West Chester University of Pennsylvania. At West Chester, he became drum major of the three-hundred-member Incomparable Golden Rams Marching Band and earned his B.A. in Music Education. "James R. Wells, director of the West Chester Band, said he admired Parks because 'on the field, he is a real ham, a showman with a definite ego-but off the field he is one of the most unassuming people you could ever meet.' He added, 'George Parks is simply the best drum major I've ever seen.' " (Kerstin Becker, <font size='1'><i>Through These Doors</i></font>)
