                    Born May 23, 1953, in Buffalo New York, George N. Parks devoted his life to inspiring generations of college and high school students to reach for the stars, to pursue music and marching band, and to become leaders and teachers in life. While growing up in Newark, Delaware, in only the ninth grade Parks decided he wanted to become a band director. Parks followed in the footsteps of his parents, Norman and Vesta Parks, who both had been drum majors, and became the drum major of the Christiana High School Viking Band. Parks later credited his band director, L. Jerome Rehberg, with having a huge impact on his life.
