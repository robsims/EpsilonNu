                <p>
                    After graduating from West Chester, Parks went to Northwestern to pursue his master's degree in tuba performance. There he studied under renowned tuba Professor Arnold Jacobs. He also acted as a graduate assistant under John P. Paynter and brought the first colorguard to the Big Ten.
                </p>
                
                <p>
                    In 1977, at just 24 years old, Mr. Parks was selected as the director of the University of Massachusetts Minuteman Marching Band by longtime marching band director John Jenkins. Mr. Parks' appointment was initially met with contest by the band members. The band experienced growing pains as Parks changed the band's marching style from the traditional Michigan styled "high-step" to a "roll-step". However, Parks' infectious enthusiasm and entertaining shows quickly won over the crowds and band members. Under his direction the band was transformed into the Power and Class of New England that is synonymous with UMass today. In 2005, Parks rewrote the lyrics to one of the university's fight songs, "Roll Down the Field." Parks often joked that being director of the UMMB was his first and only "real" job. In 1997, Old Chapel was closed and the band was forced to relocate its offices to the now condemned band apartments and then to the basement of Arnold House. For years, Parks campaigned tirelessly for a new permanent home for the band. His vision became a reality on October 17, 2009, when ground was broken for the George N. Parks Minuteman Marching Band Building.
                </p>
                
                <p>
                    During his 33 years teaching at the University of Massachusetts, Parks received numerous honors and accolades. For many years, he served as the honorary chairman for the UMass Amherst Charitable Campaign. In 1989, he received the University of Massachusetts Amherst Distinguished Teaching Award and in 1991 he was named the Massachusetts Instrumental Conductors Association Conductor of the Year and the Massachusetts Instrumental Conductors Association Hall of Fame. He was also awarded the Chancellor's Medal for Distinguished Service and was made an honorary alumnus in 1997. He was made an honorary Brother of the Epsilon Nu chapter of Kappa Kappa Psi and was awarded the A. Frank Martin award 1989. He was presented the Kappa Kappa Psi National Honorary Band Fraternity Distinguished Service to Music Medal, in the field of marching band, at the Multibands concert in October 2008.
